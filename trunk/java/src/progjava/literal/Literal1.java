package progjava.literal;

public class Literal1 {
    public static void main(String[] args) {
        int x = 00; // 0
        System.out.println(x);
        
        x = 01; // 1
        System.out.println(x);
        
        x = 010; // 8
        System.out.println(x);
        
        x = 0100; // 64
        System.out.println(x);
    }
}